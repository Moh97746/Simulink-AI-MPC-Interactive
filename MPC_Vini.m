%% Setup_My_MPC.m
% السكربت الذهبي: ضبط المتحكم ليعمل بهدوء تام مع زمن 60 ثانية

%% 1. تجهيز النموذج الداخلي (Plant Model)
% خدعة برمجية: نوهم المتحكم أن استجابة المضخة بطيئة (5 ثواني)
% هذا يجعله لا يتوقع استجابة فورية، فيرسل أوامر هادئة
Plant_Model = tf(15, [5.0 1]); 

%% 2. الزمن والأفق
Ts = 60;         % زمن العينة: 60 ثانية (مقدس لا تغيره)
p  = 300;        % أفق التنبؤ: 300 خطوة (5 ساعات)
m  = 15;         % أفق التحكم: 15 خطوة

%% 3. إنشاء الكائن (وتنظيف القديم)
if exist('MPC_Obj', 'var'), clear MPC_Obj; end
MPC_Obj = mpc(Plant_Model, Ts, p, m);

%% 4. ضبط الأوزان (علاج العصبية) 💊
% (Q) Output Weight: خفضناه جداً لـ 0.5
% المعنى: "لا تنفعل لو الهدف بعيد شوية"
MPC_Obj.Weights.Output = 0.5;

% (R) Rate Weight: رفعناه جداً لـ 20.0
% المعنى: "ممنوع التحرك بسرعة!، التغيير لازم يكون سلحفائي"
% هذه القيمة هي التي ستمنع الخجخجة عند زمن 60 ثانية
MPC_Obj.Weights.ManipulatedVariablesRate = 20.0; 

% (Input Weight): صفر
MPC_Obj.Weights.ManipulatedVariables = 0; 

%% 5. القيود (Constraints)
MPC_Obj.MV.Min = 0;
MPC_Obj.MV.Max = 1;

MPC_Obj.OV.Min = 0;
MPC_Obj.OV.Max = 16; 

%% 6. رسالة التأكيد
disp('=======================================================');
disp('✅ تم تحديث MPC_Obj بنجاح!');
disp('⚠️ هام جداً: اذهب الآن لبلوك MPC في السيمولينك');
disp('   واكتب في خانة MPC Controller الاسم: MPC_Obj');
disp('=======================================================');
